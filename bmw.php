    <h1>BMW</h1>
    <P>Not everybody knows that BMW started as a manufacturer of aircraft engines. In October 1913 Karl Friedrich Rapp establishes "Rapp-Motorenwerke" in a former bicycle factory near Munich.</P>
    <P>Rapp was an engineer who arise through the Daimler system and "Rapp-Motorenwerke" was set up as a subsidiary of "Flugwerk", an airplane maker. He starts manufacturing his own aircraft engines but unfortunately they suffered from problems with vibrations.</P>
    <P>In 1917, BMW's first aircraft engine, the Type IIIa, goes into production. It is a water-cooled six-cylinder inline engine, it features a unique "high-altitude carburetor" developed by chief engineer Max Friz that allows it to develop full power at altitude. Max Friz was a grand engineering mind who would dominate BMW's product development culture on into the 1960's.</P>
    <P>BMW became an automobile manufacturer in 1929 when it purchased Fahrzeugfabrik Eisenach, which, at the time, built Austin Sevens under licence under the Dixi marque.</P>
    <P>BMW's team of engineers progressively developed their cars from small Seven-based cars into six-cylinder luxury cars and, in 1936, began production of the BMW 328 sports car. Aircraft engines, motorcycles, and automobiles would be BMW's main products until World War II.</P>
    <P>During the war, against the wishes of its director Franz Josef Popp, BMW concentrated on aircraft engine production, with motorcycles as a side line and automobile manufacture stopped altogether.</P>
    <P>Towards the end of 1930, BMW attempted to introduce a new front axle with independent wheel suspension for both their models, the BMW 'Dixi' 3/15 DA4 and BMW 'Wartburg' DA3, but this resulted in accidents with the prototypes because of construction faults.</P>
