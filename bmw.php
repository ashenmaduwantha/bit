<h2 style="text-align:center;">2020 BMW M8</h2>
<p>We just recently discussed the verdict of the M8 Competition Coupe. The car gets high marks from the folks who were on the press drive, and now BMW has some more M8 news. The company just revealed the M8 Gran Coupe. This is the four-door coupe-like cousin of the regular BMW M8 Coupe.</p>
<p>There are two versions of the car. The M8 Gran Coupe and the M8 Competition Gran Coupe. For the most part, the cars are the same as their regular coupe counterparts. However, the car does have two more doors and an extra 200mm (almost 8 inches) of wheelbase. </p>
<p>BMW has not released the curb weight of this car, but it has to be more than the two-door model. There’s simply more of the car here. The weight was already a concern for some testers who drove it, so it may be notable. BMW has made efforts to keep weight down by utilizing carbon fiber, so the weight might not be all that much more than the two-door variant. </p>

</div>
</div>
<div class="container-fluid m-2">
    <p>In terms of performance, you can rest assured they’re both just as capable as their two-door counterparts. For example, the M8 Competition Gran Coupe is just as capable as the regular two-door model. It’ll make 0-62 mph in just 3.2 seconds and has a top speed of 155 mph. The same 4.4-liter twin-turbo V8 engine that’s in the coupe is in the gran coupe, so it’s not much of a surprise performance is still strong. </p>
