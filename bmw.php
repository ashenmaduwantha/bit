<h2 style="text-align:center;">BMW i8</h2>
<p>With all the new technologies that seem to be bursting through the pipeline these days, it has become hard to remember the time when the BMW i8 was first unveiled to the public. Released  during what feels like the olden days now, the 2014 BMW i8 revolutionized the automotive landscape by pioneering what is recognized to be the first high-performance hybrid sports car.</p>
<p>Well ahead of its time, its powertrain technology was the result of BMW’s visionary approach to a rapidly shifting narrative towards a future focused on sustainability. The eventual overthrowing of the combustion engine would be at the forefront of this movement, with EVs taking their place. The BMW i8 would be the earliest creation in this image.</p>

</div>
</div>
<div class="conatiner-fluid pt-2">
<p>The BMW i8 provides more of a transitional approach to this, than a radical one – being a plug-in hybrid as opposed to fully-electric – but would nevertheless be disrupting the status quo.  Since its 2014 release, we are still currently in the same production run based on the initial platform. </p>
<p>While the original car was relatively trailblazing for its time, BMW hasn’t made a whole lot of changes to the i8 since its 2014 release. There have been few noteworthy upgrades, if any, unless the optional Laserlight headlamps introduced in 2016 are your thing. </p>

