<p>Obviously, the new vehicle inherits all the winning features of the Coupè version. Starting with the two “sport seats” setup and the fascinating exterior lines, which characterized by sculpted and finely designed bodywork, “dress” the automotive and mechanical excellence of the model.</p>
   
