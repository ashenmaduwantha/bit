    <h1>Alfa Romeo</h1>
    <p>The below quote summarizes the impact and the profound work that this Italian automaker has given to the world. Creating history for over 100 years, Alfa Romeo started their humble beginnings as a French company in Italy (that’s a weird combination), and worked on cementing their legacy by building a storied racing heritage, supporting it with countless wins, discovering a young racer named Enzo Ferrari, and being known as a ‘petrolhead’ marque.</p>
    <p>Alfa Romeo has accomplished many wins such as “The World's Best Motor Race”, the Mille Miglia in 1928, Le Mans, various Grand Prix, World Sportscar Championship, German Touring Car Championship, just to name a few.</p>
    <p>That quote also exemplifies the craftsmanship that went to every single Alfa Romeo, painstakingly creating works of art like the P2 in 1924, the original Giulia in 1962, or the GTA in 1965.</p>
    <p>Being flamboyant as any Italian can be, the company poured their heart and soul in every creation, focusing on aesthetics and making sure that their everyday, practical models (the 155, 156, the Bretta) were jaw-dropping to make their ancestors proud.</p>
    <p>Alfa Romeos are known for their quirks and questionable reliability, but we don’t care. They’re graceful, elegant, and oh so undeniably pretty. Alongside their beauty, we embrace these cars because it’s the closest car that gives us an almost-human connection.</p>
