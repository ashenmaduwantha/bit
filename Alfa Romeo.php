    <h2 style="text-align:center;">2008 Alfa Romeo 8C Spider</h2>
    <p>19/02/2008 – Alfa Romeo once again unveils its new models onto the world market at the Geneva event. Indeed, following on from the Alfa 159 and Brera in 2005, the 159 Sportwagon and Spider in 2006, and its Personalisation Programme going by the name of “Alfa Unica” in 2007, it is now the turn of the alluring Alfa 8C Spider. Like the Alfa 8C Competizione, the Gran Turismo which received resounding global praise; the new “discovery” will be produced in a limited edition run of 500.</p>
    <p>Elaborated by the Centro Stile Alfa Romeo, the new car re-affirms the brand’s excellence in this sector, as shown by some Spider’s being regarded among the most beautiful cars in global automotive history. Cars such as the Giulietta Spider from the 50’s or the legendary Duetto made famous by “The Graduate” from 1967 with a young Dustin Hoffman. Today, it’s the Alfa 8C Spider’s turn to take centre stage and project into the future the values of technology and emotion which belong to the Alfa Romeo legacy.</p>
    
</div>
