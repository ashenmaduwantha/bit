<h2 style="text-align:center;">2015 Alfa Romeo 4C Spider</h2>
<p>Adding to the Alfa Romeo brand’s legendary racing history and the exciting experience of open-air motoring, the all-new 2015 Alfa Romeo 4C Spider debuts with panoramic views complemented with a signature Alfa exhaust note, plus the essential sportiness of the award-winning 4C Coupe: handcrafted in Modena, Italy; groundbreaking and seductive Italian style; state-of-the-art Formula 1 inspired carbon fiber monocoque chassis that enables an incredible power-to-weight ratio; advanced technologies, including the all-aluminum 1750 cc turbocharged engine with direct-injection, intercoolers, and variable-valve timing that enable supercar-level performance.</p>
<p>The Alfa Romeo 4C Spider name recalls the brand’s great sporting tradition: the acronyms 8C and 6C in the 1930s and 1940s distinguished racing and road cars fitted with powerful eight- and six-cylinder engines. Like the award-winning 4C Coupe, the all-new 2015 Alfa Romeo 4C Spider model’s designation is a return to “the great Alfa Romeos” and continues this tradition with the innovative all-aluminum 1750 cc turbocharged direct injection, intercooler four-cylinder engine, delivering an impressive 136 horsepower per liter.</p>

</div>
</div>
<div class="container-fluid m-2">
    <p>With seating for two, panoramic views and the soundtrack of an Italian performance-tuned engine and exhaust, Spiders are among the most exhilarating experiences in motoring – and represent the very core of Alfa Romeo’s DNA and heritage.</p>
    <p>The 4C Spider draws from a bloodline that includes some of the most purpose-built performance models that illustrate lightness and efficiency with undeniable Alfa Romeo style: the aluminum-bodied 8C 2900 B Touring (1938); the 1900 C52 “Flying Saucer” (1952), which could exceed 140 mph (230 kph); and most notably the legendary 33 Stradale (1967) with its mid-engine and magnesium alloy and steel tube chassis “clothed” by groundbreaking styling, which is functional and distinctly Italian. And now it is the 4C Spider’s turn to deliver the driving thrill, performance and unforgettable emotion that only an Alfa Romeo Spider provokes.</p>
