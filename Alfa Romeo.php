    <h2 style="text-align:center;">2021 Alfa Romeo Giulia GTA</h2>
    <p>As reported on EVO, there are many juicy details to dig into, so without further ado, some facts and numbers.Same Ferrari-built 2.9 liter, twin turbo, 90 degree V6 from the Quadrifoglio, has been back to the Ferrari Advanced Engineering Lab, now with power bumped to 532 HP.</p>
    <p>In the GTA, over 100 KG saved over the standard Quadrifoglio, bringing total vehicle weight to 1520 KG (3,351 lbs)</p>
    <p>In the GTAm, weight savings have gone to the extreme with the removal of the back row of seats and the fitment of a half body roll cage.</p>
   
</div>
</div>
     <div class="container-fluid">
    <p>Much of these weight savings were gained via extensive use of carbon fiber. The hood, roof, trunk lid, front spoiler, trunk spoiler (GTA), rear spoiler (GTAm), and diffuser are all made of 100% bespoke carbon fiber pieces. The GTAm also gets a cheeky little tow hook sticking out of the front, an homage to the original GTA race cars of the 50’s and 60’s.</p>
    <p>Internally, the seats are carbon fiber buckets up front with full 4 point Sabelt harnesses, and the steering wheel is now mostly leather covered carbon fiber.</p>
    <p>The entirety of the center stack and dash are also made of carbon fiber, and covered where needed in alcantara.</p>
    <p>The entire car has been dropped by about a quarter of an inch, and new bespoke springs, dampers, and suspension setup give the car a much more aggressive driving dynamic.</p>
